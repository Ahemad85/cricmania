package com.cricmania.models;

public class Ball extends Base {
	private String bowledBy;
	private String run;
	private String wicket;
	private String playedBy;
	private String commetry;
	public String getBowledBy() {
		return bowledBy;
	}
	public void setBowledBy(String bowledBy) {
		this.bowledBy = bowledBy;
	}
	public String getRun() {
		return run;
	}
	public void setRun(String run) {
		this.run = run;
	}
	public String getWicket() {
		return wicket;
	}
	public void setWicket(String wicket) {
		this.wicket = wicket;
	}
	public String getPlayedBy() {
		return playedBy;
	}
	public void setPlayedBy(String playedBy) {
		this.playedBy = playedBy;
	}
	public String getCommetry() {
		return commetry;
	}
	public void setCommetry(String commetry) {
		this.commetry = commetry;
	}
	
}
